package tales.services;




public class Task {

	
	
	
	private int id;
	private String document;
	
	
	
	
	public void setDocumentId(int id){
		this.id = id;
	}
	
	public int getDocumentId(){
		return id;
	}
	
	
	
	public void setDocumentName(String document){
		this.document = document;
	}
	
	public String getDocumentName(){
		return document;
	}
		
}
