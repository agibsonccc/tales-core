package tales.aws;




import java.util.Date;




public class RestoreObj {
	
	
	
	
	private String name;
	private Date date;
	
	
	
	
	public void setName(String name){
		this.name = name;
	}
	
	
	public String getName(){
		return name;
	}
	
	
	public void setDate(Date date){
		this.date = date;
	}
	
	
	public Date getDate(){
		return date;
	}
	
}
